<?php

namespace App\Http\Controllers\admin;

use App\Http\Controllers\Controller;
use App\Models\Comment;
use App\Models\Customer;
use App\Models\Product;
use App\Models\Rating;
use Illuminate\Http\Request;

class CommentController extends Controller
{
    /**
     * Display a listing of the resource.
     */
    public function index()
    {
        $products = Product::all();
        $comments = Comment::all();
        $ratings = Rating::all();
        return view("admin.products_management.product-list",compact("products","comments","ratings"));
    }

    /**
     * Show the form for creating a new resource.
     */
    public function create() {
        $customers = Customer::all(); 
        $products = Product::all();
        return view('admin.comment.add', compact('customers', 'products'));
    }


    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
         $request->validate([
            'customer_id' => 'required|exists:customers,id',
            'product_id' => 'required|exists:products,id',
            'content' => 'required|string',
        ]);

        Comment::create($request->only('customer_id','product_id','content'));

        return redirect()->route('admin.comment.index')->with('success','Comment added successfully.');
    }

    /**
     * Display the specified resource.
     */
    public function show(Comment $comment)
    {
        $comments = Comment::all();
        return view('admin.comment.show', compact('comments'));
    }

    /**
     * Show the form for editing the specified resource.
     */
    public function edit(Comment $comment)
    {
        $customers = Customer::all();
        $products = Product::all();
        return view('admin.comment.edit', compact('comment','customers','products'));
    }

    /**
     * Update the specified resource in storage.
     */
    public function update(Request $request, Comment $comment)
    {
        $request->validate([
            'customer_id' => 'required|exists:customers,id',
            'product_id' => 'required|exists:products,id',
            'content' => 'required|string',
        ]);

        $comment->update($request->only('customer_id','product_id','content'));

        return redirect()->route('admin.comment.index');
    }

    /**
     * Remove the specified resource from storage.
     */
    public function destroy(Comment $comment)
    {
        $comment->delete();
        return redirect()->route('admin.comment.index');
    }
}
